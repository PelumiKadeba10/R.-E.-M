const formatDate = (data) => {
  const day = data.getDate();
  const month = data.toLocaleString('default', { month: 'long' });
  const year = data.getFullYear();

  const suffix = (day) => {
    if (day > 3 && day < 21) return 'th'; // Covers 4th to 20th
    switch (day % 10) {
      case 1: return 'st';
      case 2: return 'nd';
      case 3: return 'rd';
      default: return 'th';
    }
  };

  return `${day}${suffix(day)} ${month}, ${year}`;
};

